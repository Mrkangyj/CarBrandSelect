    private void initView() {
        mListView = findViewById(R.id.list_view);
        mChildListView = findViewById(R.id.child_list_view);
        mLetterBar = findViewById(R.id.side_letter_bar);
        mDrawerLayout = findViewById(R.id.drawer_layout);
        mCarBrand = findViewById(R.id.car_brand);
        mDrawerLayout.setScrimColor(getResources().getColor(R.color.color_b3000));
        mDrawerLayout.setDrawerLockMode(DrawerLayout.LOCK_MODE_LOCKED_CLOSED);
    }

    private void initData() {
        CarBrand carBrand = gson.fromJson(json, CarBrand.class);
        CarSries carSries = gson.fromJson(childJson, CarSries.class);

        allList.addAll(carBrand.getData().getAll());
        hotList.addAll(carBrand.getData().getHot());
        childList.addAll(carSries.getInfo());

        mAdapter = new CarBrandAdapter(this, allList, hotList);
        mListView.setAdapter(mAdapter);

        mChildAdapter = new CarSeriesAdapter(this, childList);
        mChildListView.setAdapter(mChildAdapter);
    }


    private void initListener() {
        mLetterBar.setOnLetterChangedListener(new SideLetterBar.OnLetterChangedListener() {
            @Override
            public void onLetterChanged(String letter) {
                int position = mAdapter.getLetterPosition(letter);
                mListView.setSelection(position);
            }
        });

        mAdapter.setOnItemClickListener(new CarBrandAdapter.OnItemClickListener() {
            @Override
            public void onAllClick(CarBrand.DataBean.AllBean infoBean) {
                showDrawerLayout();
                mCarBrand.setText(infoBean.getName());
            }

            @Override
            public void onHotClick(CarBrand.DataBean.HotBean infoBean) {
                showDrawerLayout();
                mCarBrand.setText(infoBean.getName());
            }


        });
        mChildListView.setOnItemClickListener(new AdapterView.OnItemClickListener() {
            @Override
            public void onItemClick(AdapterView<?> parent, View view, int position, long id) {
                showDrawerLayout();
                Toast.makeText(MainActivity.this, childList.get(position).getName(), Toast.LENGTH_SHORT).show();
            }
        });
    }

    private void showDrawerLayout() {
        if (!mDrawerLayout.isDrawerOpen(Gravity.RIGHT)) {
            mDrawerLayout.openDrawer(Gravity.RIGHT);
        } else {
            mDrawerLayout.closeDrawer(Gravity.RIGHT);
        }
    }



